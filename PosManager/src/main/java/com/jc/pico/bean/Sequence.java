package com.jc.pico.bean;

public class Sequence {
	
	private String  seqId;
	private String  seqToday;
	private String  seqOrderNo;
	private Integer seqReceiptNo;
	
	
	public String getSeqId() {
		return seqId;
	}
	public void setSeqId(String seqId) {
		this.seqId = seqId;
	}

	public String getSeqToday() {
		return seqToday;
	}
	public void setSeqToday(String seqToday) {
		this.seqToday = seqToday;
	}
	
	public String getSeqOrderNo() {
		return seqOrderNo;
	}
	public void setSeqOrderNo(String seqOrderNo) {
		this.seqOrderNo = seqOrderNo;
	}
	
	public Integer getSeqReceiptNo() {
		return seqReceiptNo;
	}
	public void setSeqReceiptNo(Integer seqReceiptNo) {
		this.seqReceiptNo = seqReceiptNo;
	}

	
}
